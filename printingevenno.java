public class printingevenno 
{

    public static void main(String[] args) 
    {

        //  print all the even no from 1 to 30 

        for(int i=0; i<=30; i++)
        {

            if(i%2==0)
            {
                System.out.println(i+" is even number");
            }
            else
            {
                System.out.println(i+" is odd number");
            }
        }
        
    }
    
}
